package com.yh.bean;

public class hospital {

	private int hid;
	
	private String hname;
	
	private String hst_address;
	
	private String hcity;
	
	private String hstate;
	
	private int hzip;

	public int getHid() {
		return hid;
	}

	public void setHid(int hid) {
		this.hid = hid;
	}

	public String getHname() {
		return hname;
	}

	public void setHname(String hname) {
		this.hname = hname;
	}

	public String getHst_address() {
		return hst_address;
	}

	public void setHst_address(String hst_address) {
		this.hst_address = hst_address;
	}

	public String getHcity() {
		return hcity;
	}

	public void setHcity(String hcity) {
		this.hcity = hcity;
	}

	public String getHstate() {
		return hstate;
	}

	public void setHstate(String hstate) {
		this.hstate = hstate;
	}

	public int getHzip() {
		return hzip;
	}

	public void setHzip(int hzip) {
		this.hzip = hzip;
	}
}
