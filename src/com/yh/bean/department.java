package com.yh.bean;

public class department {
	
	private int did;
	
	private String dname;
	
	private String dtel;
	
	private int hospital_hid;

	public int getDid() {
		return did;
	}

	public void setDid(int did) {
		this.did = did;
	}

	public String getDname() {
		return dname;
	}

	public void setDname(String dname) {
		this.dname = dname;
	}

	public String getDtel() {
		return dtel;
	}

	public void setDtel(String dtel) {
		this.dtel = dtel;
	}

	public int getHospital_hid() {
		return hospital_hid;
	}

	public void setHospital_hid(int hospital_hid) {
		this.hospital_hid = hospital_hid;
	}
}
