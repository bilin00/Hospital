package com.yh.bean;

public class physician {

	private int phid;
	
	private String phfname;
	
	private String phtel;
	
	private String phspl;
	
	private int hid;

	public int getPhid() {
		return phid;
	}

	public void setPhid(int phid) {
		this.phid = phid;
	}

	public String getPhfname() {
		return phfname;
	}

	public void setPhfname(String phfname) {
		this.phfname = phfname;
	}

	public String getPhtel() {
		return phtel;
	}

	public void setPhtel(String phtel) {
		this.phtel = phtel;
	}

	public String getPhspl() {
		return phspl;
	}

	public void setPhspl(String phspl) {
		this.phspl = phspl;
	}

	public int getHid() {
		return hid;
	}

	public void setHid(int hid) {
		this.hid = hid;
	}
}
